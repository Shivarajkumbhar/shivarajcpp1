#include<iostream> 
using namespace std; 
int main()
{
// create variables 
  int p = 10, q = 15;
// == operator
cout<<(p == q); // false
// != operator
cout<<(p != q); // true
// > operator
cout<<(p > q); // false
// < operator
cout<<(p < q); // true
// >= operator
cout<<(p >= q); // false
// <= operator
cout<<(p <= q); // true
}
